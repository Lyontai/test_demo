                  calling and in philanthropy.
                  <p>He started his nursery and primary school Education at the Adeolu Nursery and Primary school Owo Ondo state, established by the late Chief Adekunle and Mrs. Olufunke Ajasin and he eventually completed his primary school Education at government primary school Owo in 1982. He proceeded to the Apata community Grammar school Apata Ibadan in 1982 where he completed his secondary School Education in 1987. He attended the government college Ibadan briefly for his higher education in 1988 before he proceeded to the university of Ibadan to study geology between 1988-1990. While studying geology, Dr. Oshodi developed an unusual interest in studying medicine hence, he secretly wrote the entrance examination popularly called JAMB at the time and he gained admission to the University of Benin to study Medicine in 1990 and graduated in 1998, he completed his houseman-ship at the Lagos State University teaching hospital Ikeja in 1999 and completed his national youth service at Otta General Hospital Ogun state in the year 2000.</p>
                  <p>As a pragmatic personality with ability to access situation and thereby make informed decision based on what would be of great benefits to himself and humanity coupled with his quest for more knowledge, Dr. Oshodi thereafter proceeded to the Republic of Ireland in 2001 for his postgraduate training. He was admitted to Royal College of Surgeon Ireland for the four years psychiatry residency rotational training scheme from 2003 till 2007. Dr. Oshodi relocated to Canada in 2012 to set up his private psychiatry practice and he obtained the Canadian psychiatry Fellowship in 2014 and he was appointed an assistant Professor of Psychiatry in 2014.</p>
                  <p>This versatile and industrious man of integrity has been described as a caring, benevolent and cheerful giver who would go the extra mile in making far-reaching decisions to impact positively on his people for the benefit of mankind through his Foundation- The Dr. Abiola Oshodi Foundation. This Foundation has in various ways transformed so many lives as countless testimonies abound.
                    </p>
                    <p>Dr. Oshodi is married to Mrs. Tolulope Oshodi and they are blessed with two Children Ayomide Oshodi and Ayobami Oshodi. Dr. Oshodi is the CEO of Dr. Oshodi specialist Services and the Dr. Abiola Oshodi Foundation, The Triple A&T Farm.</p>
